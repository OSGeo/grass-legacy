      SUBROUTINE RMTIME(TIM)
      INCLUDE 'TEXT.DCK'
      INCLUDE 'SYSPAR.DCK'
C 
C     *** VAX SYSTEM DEPENDENT ROUTINE ***
C
C  PURPOSE:   RETURN THE CURRENT TIME AS INTEGER (SEC FROM MIDNIGHT)
C
C  PARAMETERS:
C         TIM-----THE CURRENT TIME
C
      CHARACTER*8 TBUF
      CALL TIME(TBUF)
      READ(TBUF,'(I2,1X,I2,1X,I2)') H,M,S
      TIM = H*3600 + M*60 + S
      RETURN
      END
